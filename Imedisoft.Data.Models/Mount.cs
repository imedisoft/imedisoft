using Imedisoft.Data.Annotations;
using System;
using System.Collections.Generic;
using System.Drawing;

namespace Imedisoft.Data.Models
{
    [Table("mounts")]
	public class Mount
	{
		[PrimaryKey]
		public long Id;

		public long PatientId;

		[ForeignKey(typeof(Definition), nameof(Definition.Id))]
		public long Category;

		/// <summary>
		/// Used to provide a document description in the image module tree-view.
		/// </summary>
		public string Description;

		/// <summary>
		/// To allow the user to enter specific information regarding the exam and tooth numbers, as well as points of interest in the xray images.
		/// </summary>
		public string Note;

		public int Width;

		public int Height;

		/// <summary>
		/// Color of the mount background. Typically white for photos and black for radiographs.
		/// </summary>
		public Color BackColor;

		[Column(AutoGenerated = true)]
		public DateTime AddedOn;

		public List<MountItem> ListMountItems { get; set; }
	}
}
